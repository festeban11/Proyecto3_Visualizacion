CREATE TABLE transacciones (
    id INT PRIMARY KEY,
    usuario VARCHAR(255),
    fecha DATE,
    hora TIME,
    fechahora DATETIME UNIQUE,
    idconsulta INT,
    tipoconsulta VARCHAR(255),
    medico VARCHAR(255),
    idpaciente INT,
    prevision VARCHAR(255),
    iddetalle INT,
    detalle VARCHAR(255),
    abono FLOAT,
    devolucion FLOAT,
    mpago FLOAT,
    tbono VARCHAR(255),
    pagacon VARCHAR(255),
    isapre VARCHAR(255),
    numbono VARCHAR(255),
    voucher VARCHAR(255),
    numboleta VARCHAR(255),
    notadecredito INT,
    monto INT,
    fechagarantia DATE,
    estado INT,
    totalPago INT,
    totalBonif INT,
    totalSeg INT,
    totalCopago INT,
    numero_mes INT,
    nombre_mes VARCHAR(255),
    trimestre INT,
    semestre INT,
    FOREIGN KEY (usuario) REFERENCES cajeros(rut_cajero),
    FOREIGN KEY (medico) REFERENCES medicos(rut_medico)
);

CREATE TABLE medicos (
    rut_medico VARCHAR(255) PRIMARY KEY,
    nombre_medico VARCHAR(255)
);

CREATE TABLE cajeros (
    rut_cajero VARCHAR(255) PRIMARY KEY,
    nombre_cajero VARCHAR(255)
)

